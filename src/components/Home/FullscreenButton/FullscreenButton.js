import React, { Component } from 'react';
import './FullscreenButton.css';

export default class FullscreenButton extends Component {
	state = {
		isFullscreen: false
	}

	componentDidMount = () => {
		let isFull = document.webkitFullscreenElement;
		if(isFull) {
			this.setState({isFullscreen: true})
		}
	}

	fullscreenToggle = (e) => {
		this.state.isFullscreen ? document.webkitExitFullscreen()
														: document.documentElement.webkitRequestFullscreen();

		this.setState((prevState) => {
			return ({isFullscreen: !prevState.isFullscreen})
		})
	}

	render() {
		return(
			<div>
				{this.state.isFullscreen ? (
					<div id='FullscreenButton' className='FullscreenButton--close' onClick={this.fullscreenToggle} >
							<CloseFull/>
					</div>
					) : (
					<div>
						<div id='FullscreenButton' className='FullscreenButton--open' onClick={this.fullscreenToggle} >
								<OpenFull/>
						</div>
					</div>
					)
				}
			</div>
		)
	}
}

